import UIKit

extension UIFont {
    
    static func fontForSectionTitle() -> UIFont {
        
        return UIFont(name: "AvenirNextCondensed-Bold", size: 24)!
        
    }
    
    static func fontForDateHeader() -> UIFont {
        
        return UIFont(name: "AvenirNextCondensed-Bold", size: 18)!
        
    }
    
}
